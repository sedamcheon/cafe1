const DEFAULT_UPDATED_AT = "2026-06-11";

function menu(name, category, price) {
  return {
    name,
    category,
    price,
    updatedAt: DEFAULT_UPDATED_AT
  };
}

function makeMenus(items) {
  return Object.fromEntries(
    items.map((item, index) => {
      const key = `menu_${String(index + 1).padStart(3, "0")}`;
      return [key, menu(item[0], item[1], item[2])];
    })
  );
}

